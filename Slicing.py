#what is slicing:-   Slicing is a technique used in Python to extract a subset of elements
 
#from a sequence (like a list, string, or tuple) by specifying a range of indices. 

#It allows you to create a new sequence containing only the elements you're interested in.

#EXAMPLES OF LIST SLICING:-

list1 = ["A", "B", "C", "D", "E", "F", "G"]
print("#THIS  IS ALL ARE EXAMPLES OF LIST SLICING")

print(list1[0])
print(list1[-1])

print(list1[-2])

print(list1[5:])

print(list1[2:6])

print(list1[:-3])

print(list1[-3:])

print(list1[-5:-2])

print(list1[1])

print(list1[0:3])

print(list1[-3:-2])



